function generatePersonalityProfile(engineTraits, interfaceTraits, safeModeTraits) {
    // 3つのOSのデータから統合的な人物像を生成
    const engine = engineTraits || HexagramHumanTraits["乾為天"];
    const inter = interfaceTraits || HexagramHumanTraits["坤為地"];
    const safe = safeModeTraits || HexagramHumanTraits["水雷屯"];
    
    // データ検証
    if (!engine?.personality || !inter?.personality || !safe?.personality) {
        return {
            summary: "あなたは多面的で複雑な個性を持つ人物です。",
            detail: "分析データが不足していますが、独自の魅力があります。"
        };
    }
    
    return {
        summary: `${engine.personality.core}の本質を持ちながら、${inter.personality.core}として社会と関わり、${safe.personality.core}の側面で安定を保つ、バランスの取れた人物です。`,
        innerDrive: engine.personality.summary,
        socialFace: inter.personality.summary,
        foundation: safe.personality.summary,
        workStyle: `仕事では${engine.behavioral.work}`,
        relationshipStyle: `人間関係では${inter.behavioral.relationships}`,
        stressManagement: `ストレス対処は${safe.behavioral.stress}`,
        growthPath: {
            engine: engine.growth.advice,
            interface: inter.growth.advice,
            safeMode: safe.growth.advice
        }
    };
}

