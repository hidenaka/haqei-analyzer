function getLineName(hexagram, position) {
  const hex = hexagramsToFix.find(h => h.name === hexagram);
  if (!hex) return null;
  
  const positionNames = ['初', '二', '三', '四', '五', '上'];
  return positionNames[position] + hex.lines[position];
}

const newNarratives = [];
const patterns = ['JJJ', 'JJH', 'JHJ', 'JHH', 'HJJ', 'HJH', 'HHJ', 'HHH'];

hexagramsToFix.forEach(hexagram => {
  // 二、三、四、五の爻だけを生成（初と上は既存）
  [1, 2, 3, 4].forEach(position => {
    const lineName = getLineName(hexagram.name, position);
    
    patterns.forEach(pattern => {
      const key = `${hexagram.name} ${lineName} | ${pattern}`;
      
      // 既存データにない場合のみ生成
      if (!existingData[key]) {
        const narrative = generateNarrative(hexagram.name, position, lineName, pattern);
        newNarratives.push({ key, narrative });
      }
    });
  });
});

